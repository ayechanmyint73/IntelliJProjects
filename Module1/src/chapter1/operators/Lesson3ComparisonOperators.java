package chapter1.operators;

public class Lesson3ComparisonOperators {
    public static void main(String[] args) {

        //comparison operators ( < > <= >= == != )
        int x = 50, y = 34;
        boolean result;

        result = x < y ;
        System.out.println(result);

        result = x > y ;
        System.out.println(result);

        result = x <= y ;
        System.out.println(result);

        result = x >= y ;
        System.out.println(result);

        result = x == y ;
        System.out.println(result);

        result = x != y ;
        System.out.println(result);
    }
}
