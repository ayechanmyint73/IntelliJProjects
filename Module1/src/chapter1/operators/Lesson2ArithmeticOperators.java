package chapter1.operators;

public class Lesson2ArithmeticOperators {
    // Arithmetic operators ( + - * / % )

    public static void main(String[] args) {
        int a = 30, b = 8;
        int result;

        System.out.println(a + b);
        result = a + b;
        System.out.println(result);

        result = a - b;
        System.out.println(result);

        result = a * b;
        System.out.println(result);

        result = a / b;
        System.out.println(result);

        result = a % b;
        System.out.println(result);
    }
}
