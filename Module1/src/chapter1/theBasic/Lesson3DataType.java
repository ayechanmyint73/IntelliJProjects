package chapter1.theBasic;

public class Lesson3DataType {

    public static void main(String[] args) {

        byte by = 125;
        short sh = 1234;
        int in = 19999;
        long lg = 1111111111111L;
        //when the user declare the variable with long, at the end of the value L has to be added.

        float ft = 8.56f;
        //when the user declares the variable with float, at the end of the value "f" has to be added.

        double db = 88.45;

        boolean value = true;

        char ch = 98;
        System.out.println(ch);
        System.out.println(lg);
        System.out.println(ft);
    }
}

